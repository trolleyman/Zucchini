package game.render;

/**
 * Alignment point to be used on/with images and shapes being rendered. 
 * First letter corresponds to vertical alignment point.
 * Second letter corresponds to horizontal alignment point.
 */
public enum Align {
	/**
	 * Top Left
	 */
	TL, 
	
	/**
	 * Top Middle
	 */
	TM, 
	
	/**
	 * Top RIght
	 */
	TR,
	
	/**
	 * Middle Left
	 */
	ML, 
	
	/**
	 * Middle Middle (center)
	 */
	MM, 
	
	/**
	 * Middle Right
	 */
	MR,
	
	/**
	 * Bottom Left
	 */
	BL, 
	
	/**
	 * Bottom Middle
	 */
	BM, 
	
	/**
	 * Bottom Right
	 */
	BR
}
